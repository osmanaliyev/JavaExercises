
public class Conditionals {

	public static void main(String[] args) {
		
		double value = 100.3;
			if (value > 100) {
				System.out.println("This number is bigger than hundred");
			} 
			else if (value == 100) {
				System.out.println("This number is equal to 100");
				
			}
			else {
				System.out.println("This number less than hundred");
			}

	}

}
